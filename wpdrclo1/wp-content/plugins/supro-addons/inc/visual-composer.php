	/**
	 * Enqueue icon element font
	 *
	 * @param $font
	 */
	function vc_icon_element_fonts_enqueue( $font ) {
		switch ( $font ) {
			case 'ionicons':
				wp_enqueue_style( 'ionicons' );
				break;

			case 'linearicons':
				wp_enqueue_style( 'linearicons' );
				break;
		}
	}

	function vc_iconpicker_base_register_css() {
		wp_enqueue_style( 'ionicons', SUPRO_ADDONS_URL . '/assets/css/ionicons.min.css', array(), '2.0.0' );
		wp_enqueue_style( 'linearicons', SUPRO_ADDONS_URL . '/assets/css/linearicons.min.css', array(), '1.0.0' );
	}
